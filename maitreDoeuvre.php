            <h1>LE CABINET EXCELT@X : MAITRE DOEUVRE DU PROJET  </h1>
            <p>Depuis une douzaine d'années, l'Etat a progressivement développé une politique d'amélioration des relations entre l'administration
            fiscale et les usagers ; particuliers et entreprises. Il s'est agit à la fois de favoriser l'acceptation de l'impôt par les contribuables,
             de mieux sécuriser le recouvrement des recettes et d'optimiser les charges de gestion de l'impôt  <br>
            Ayant soutenue un mémoire en vue de l’obtention d’un DESS en fiscalité appliqué sur le thème intitulé : « Le contrôle fiscal dans les 
            entreprises camerounaises : Analyse des freins au partenariat administration fiscale et contribuable » ; nous avons noté une profonde inertie
             dans ce domaine et un manque de stratégie de partenariat efficace influençant ainsi la discipline fiscale ou son contraire l’indiscipline fiscale. 
             Cette volonté de compréhension des phénomènes et des comportements, nous a amené à identifier les principaux chefs de redressements suite au contrôle 
             fiscal des contribuables ainsi que les dysfonctionnements entravant la relation administration fiscale et contribuable. Le Cabinet EXCELT@X & 
             PARTNERSHIPS SARL est votre interlocuteur unique d’encadrement et d’assistance fiscale. Notre mission s’inscrit dans le cadre des dispositions de 
             l’acte N°30/84-UDEAC-398 du 18 décembre 1984 portant statut des Conseils fiscaux et des textes modificatifs subséquents en vertu de la décision
              N°129/17-UEAC-910-D-CM-SE du 13 novembre 2017 du conseil des Ministres de l’Union Économique de l’Afrique Centrale (UEAC) portant accord d’agrément
             en qualité de Conseil Fiscal sous le numéro CF 240 du tableau. Cette orientation vise à instaurer des rapports de confiance entre l’administration 
             fiscale et ses usagers en cherchant la satisfaction du contribuable, et la facilitation de son consentement à l’impôt </p>
             <p>Véritable interface entre l’administration et le contribuable ; le Programme Partenariat Fiscalo Comptable personnalisé
             des PME est un service juridique, comptable, fiscale et social pour booster les activités en toute sécurité. Il se propose de regrouper les contribuables
             sur la même plateforme en vue de leur apporter un suivi personnalisé dans la gestion et la fiscalité de leur activité. </p>
             <p>En honorant ses obligations comptables et fiscales, chaque contribuable participe, selon sa situation, à financer les services offerts à la collectivité. 
            Il contribue ainsi au développement du civisme fiscal dans notre pays, ce qui suppose l’existence d’une relation responsable et personnalisée entre le Contribuable et l’Administration.</p>
            <p>Tout entité est assujettie à des textes légaux et réglementaires dont le non-respect peut conduire à des anomalies significatives dans les comptes. La correction de ces anomalies par 
            l’administration (Impôts, Douanes, CNPS …) entraîne des pertes financières énormes suite au contrôle ou la fermeture de l’entité le cas échéant. <br>
            La survie de l’entité dépend désormais de sa capacité à maîtriser et gérer ses risques fiscaux.Le non-respect des dispositions réglementaires permet 
            de comprendre que la taxation d’office ou des sanctions financières lourdes pèsent sur votre entité.
        </p>
