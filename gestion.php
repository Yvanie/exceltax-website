            <h1>NOS POLES DE GESTION </h1>
            <p><span>En effet, la loi n° 2010/001 du 13 avril 2010 modifiée par la loi n°2015/010 du 16 juillet 2015 portant
                 Promotion des PME</span>, regroupe sous le vocable de PME, l’ensemble des entités économiques qui emploient
                au plus 100 personnes et dont le chiffre d’affaires n’excède pas trois milliards de FCFA.  <br>
                Le dispositif concerne toutes les PME au sens de la loi <span>N° 2015/010 DU 16 juillet 2015 modifiant</span> et 
                complétant certaines dispositions <span>la loi N° 2010/001 DU 13 AVRIL 2010 portant promotion des petites et
                 moyennes entreprises au Cameroun. </span>
        </p>
        <ul>
            <li>Une très Petite Entreprise (TPE)est une entreprise ayant au plus 5 employés et dont le chiffre d’affaires est de moins de 15 millions de FCFA ; </li>
            <li> Une Petite Entreprise (PE)est une entreprise dont l’effectif des employés est compris entre 6 et 20 et, ayant un chiffre d’affaires compris entre 15 et 250 millions de FCFA;</li>
            <li>Une Moyenne Entreprise (ME)est une entreprise dont l’effectif des employés est compris entre 21 et 100 et, ayant un chiffre d’affaire supérieur à 250 millions et inférieur ou égal à 3 milliards de FCFA ; </li>
            <li>Une Grande Entreprise (GE)est une entreprise dont l’effectif des employés est supérieur à 100 et dont le chiffre d’affaires est de plus de 3 milliards de FCFA </li>
        </ul>
        <p>
        Les PME sont organisées par pole de gestion ; ce qui permet l’émergence de forte spécialisation. A l’intérieur de nos bureaux, les agents sont organisés par équipes pluridisciplinaires : certains agents jouent le rôle de « clients managers » (gestionnaire des comptes clients) » <br>
        En outre, la segmentation de la clientèle, ou encore la « customisation » du service offert, constitue un moyen pour le Cabinet de se rapprocher des besoins spécifiques de ses clients, et de devenir au-delà de la simple rhétorique, véritablement « orientée vers les clients ». <br>
        Dans leur qualité de service aux clients (contribuables), nous retenons le concept d’Interlocuteur Fiscal Unique (Guichet Unique Fiscal pour l’ensemble des problèmes fiscaux du contribuable assiette ou recouvrement) : ce contact clientèle constitue le point d’entrée unique du contribuable dans ses relations avec le fisc.</p>
