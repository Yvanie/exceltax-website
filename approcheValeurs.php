            <h1>NOTRE APPROCHE ET VALEURS</h1>
            <p>Conformément à notre profession et ses normes professionnelles, différents types de missions-vous sont proposés avec un niveau d’intervention 
            variable et adapté à vos besoins. Tous nos travaux sont restitués avec un rapport et une attestation.
            Nos relations sont formalisées par la signature d’un contrat de mission où nos honoraires et les obligations réciproques sont clairement définies. En tenant de 
            la spécificité de chaque entité, les missions sont regroupées autour d’un ensemble d’offres modulables. <br>
            Tout en étant à la fois le Médecin fiscal et l’avocat de votre entreprise ; en ayant une vue systémique de l’entreprise (Approche Guichet Unique), notre but est : </p>
            <ul>
                <li>D’identifier le problème réel de l’organisation en matière de comptabilité et fiscalité, et à travers un processus ouvert et honnête, d’essayer d’élaborer des solutions. 
                    Une fois qu’une solution spécifique a été trouvée, nous collaborons à sa mise en œuvre. Les tâches se regroupent autour de : Audit de votre système de gestion et d’information;
                    proposition d’une solution complète, mise en œuvre, formation, suivi et évaluation.;</li>
                <li>Recevoir de l’organisation mandat de représentation auprès de l’administration fiscale et autres pour tous les problèmes de fiscalité et parafiscalité. </li>
            </ul> 
         <p>
    Notre coaching est toujours accompagné de conseils pratiques pour l’organisation, une expérience gagnant-gagnant.Notre démarche privilégie le dialogue a priori plutôt que contrôle a posteriori. 
    On privilégie la contemporanéité plutôt que le redressement a posteriori, en donnant la possibilité aux entreprises de discuter préalablement de leurs options fiscales avec nous, avant de les 
    adopter ou non. L’interlocuteur des entreprises devient dès lors moins un organe de contrôle que de dialogue, tant au niveau de l’entreprise, l’administration fiscale de l’équipe du Cabinet
 EXCELTAX chargé de l’accompagnement fiscal des PME. <br>
 Outre ces deux mesures phares (partenariat fiscal et accompagnement personnalisé des PME), certaines directions parmi lesquelles un appui des entreprises à l’international dans leurs difficultés avec les administrations fiscales étrangères, mais aussi : </p>
<ul>
    <li>Une démarche spontanée de mise en conformité, qui sera assurée par un service de mise en conformité fiscale, pour traiter leurs déclarations rectificatives sur des sujets complexes ; </li>
    <li>Un examen de conformité fiscale par un tiers de confiance pour sécuriser des points usuels de fiscalité présentant de fortes convergences avec la comptabilité ; </li>
    <li>Une mobilisation pour les rescrits ; </li>
    <li>Une amélioration du dialogue et des recours dans le contrôle.</li>
</ul> 
<p>Nous mettons en avant la volonté d’instaurer une relation fondée « sur la confiance, la transparence et la contemporanéité », sous forme d’un « dialogue continu avec l’administration sur des points déterminés conjointement » avec les entreprises qui se prêteront au jeu. </p>
